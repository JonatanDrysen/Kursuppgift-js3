import React, { useState } from 'react';
import { useNavigate } from 'react-router-dom';
import ContainerLogIn from '../components/styled/ContainerLogIn';
import Header1 from '../components/styled/Header1';
import LoginButton from '../components/styled/LoginButton';

export default function LoginPage() {
    const [email, setEmail] = useState('')
    const [password, setPassword] = useState('')
    const navigate = useNavigate()

    function handleOnSubmit(e) {
        e.preventDefault()
        const url = 'https://frebi.willandskill.eu/api-token-auth/'
        const payload = { email, password }
        fetch(url,  {
            method: 'POST',
            headers: {
                'Content-type': 'application/json'
            },
            body: JSON.stringify(payload)
        })
        .then(res => res.json())
        .then(data => {
            const token = data.token
            localStorage.setItem('webb21-kursuppgift-js3', token)
            navigate('/home')
        })
    }

    return (
        <div>
            <Header1 text='Kund-o-Matic | Log in' />
            <ContainerLogIn>
                <form onSubmit={handleOnSubmit}>
                    <input
                        type='text'
                        value={email}
                        placeholder='E-Mail'
                        onChange={e => setEmail(e.target.value)}
                    />
                    <input
                        type='password'
                        value={password}
                        placeholder='Password'
                        onChange={e => setPassword(e.target.value)}
                    />
                    <LoginButton type='submit'>Log in</LoginButton>
                </form>
            </ContainerLogIn>
        </div>
    )
}
